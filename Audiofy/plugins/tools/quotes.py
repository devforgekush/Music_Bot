# Helper Function: Anime Quote Image
async def get_anime_image_quote():
    url = "https://animechan.xyz/api/random/image"
    async with aiohttp.ClientSession() as session:
        async with session.get(url) as resp:
            if resp.status == 200:
                image_bytes = await resp.read()
                return BytesIO(image_bytes)
            return None

