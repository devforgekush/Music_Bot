# /aquotes тЖТ Text Anime Quote
@app.on_message(filters.command("aquotes"))
async def anime_quote_text(_, message: Message):
    await message.reply_chat_action(ChatAction.TYPING)
    quote = await get_anime_quote()
    if quote:
        await message.reply_text(quote)
    else:
        await message.reply_text("тЭМ Error fetching quote. Try again later.")

